package com.sr03.dao;

import com.sr03.entities.UserEntity;

import java.sql.*;

import static com.sr03.dao.DAOUtility.*;

public class UserDAO extends DAO<UserEntity> {
    private static final String SQL_SELECT_BY_EMAIL = "SELECT * FROM users WHERE email = ?";
    private static final String SQL_INSERT = "INSERT INTO users (email, name, password, company, phone, created_at, is_active, is_admin) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
    private static final String SQL_UPDATE = "UPDATE users SET email = ?, name = ?, password = ?, company = ?, phone = ?, is_active = ?, is_admin = ? WHERE id = ?";
    private static final String SQL_CHANGE_ACTIVITY = "UPDATE users SET is_active = (is_active + 1)%2 WHERE id = ?";
    private static final String SQL_STATS_BY_USER = "SELECT SUM(a.is_correct)*100/COUNT(*) AS global_stats\n" +
            "FROM records r\n" +
            "INNER JOIN users_answers ua\n" +
            "\tON r.id = ua.record_id\n" +
            "INNER JOIN questions q\n" +
            "\tON q.id = ua.question_id\n" +
            "INNER JOIN answers a\n" +
            "\tON a.question_id = q.id\n" +
            "WHERE r.user_id = ?\n" +
            "\tAND ua.answer_id = a.id";
    private static final String SQL_AVG_TIME = "SELECT STR_TO_DATE(AVG(TIMEDIFF(finished_at, started_at)), \"%s\") AS avg_time\n" +
            "FROM records r\n" +
            "WHERE r.user_id = ?";
    private static final String SQL_SUM_TIME = "SELECT STR_TO_DATE(SUM(TIMEDIFF(finished_at, started_at)), \"%s\") AS sum_time\n" +
            "FROM records r\n" +
            "WHERE r.user_id = ?";

    UserDAO(DAOFactory daoFactory) {
        super(daoFactory, "users");
    }

    @Override
    public UserEntity map(ResultSet resultSet) {
        UserEntity user = new UserEntity();
        try {
            user.setId(resultSet.getLong("id"));
            user.setIs_active(resultSet.getBoolean("is_active"));
            user.setIs_admin(resultSet.getBoolean("is_admin"));
            user.setName(resultSet.getString("name"));
            user.setEmail(resultSet.getString("email"));
            user.setPassword(resultSet.getString("password"));
            user.setCompany(resultSet.getString("company"));
            user.setPhone(resultSet.getString("phone"));
            user.setCreated_at(resultSet.getTimestamp("created_at"));
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return user;
    }

    @Override
    public void create(UserEntity user) throws DAOException {
        Connection conn = null;
        PreparedStatement preparedStatement = null;
        ResultSet autoGeneratedValues = null;

        try {
            /* Récupération d'une connexion depuis la Factory */
            conn = daoFactory.getConnection();
            preparedStatement = initPreparedStatement(conn, SQL_INSERT, true,
                    user.getEmail(),
                    user.getName(),
                    user.getPassword(),
                    user.getCompany(),
                    user.getPhone(),
                    user.getCreated_at(),
                    user.getIs_active(),
                    user.getIs_admin()
            );

            int status = preparedStatement.executeUpdate();
            if (status == 0) {
                throw new DAOException("Échec de la création de l'utilisateur.");
            }

            autoGeneratedValues = preparedStatement.getGeneratedKeys();
            if (autoGeneratedValues.next()) {
                user.setId(autoGeneratedValues.getLong(1));
            } else {
                throw new DAOException("Échec de la création de l'utilisateur.");
            }
        } catch (SQLException e) {
            throw new DAOException(e);
        } finally {
            silentClosures(autoGeneratedValues, preparedStatement, conn);
        }
    }

    public UserEntity get(String email) throws DAOException {
        return getQuery(SQL_SELECT_BY_EMAIL, email);
    }

    @Override
    public void update(UserEntity user) throws DAOException {
        Connection conn = null;
        PreparedStatement preparedStatement = null;

        try {
            conn = daoFactory.getConnection();
            preparedStatement = initPreparedStatement(conn, SQL_UPDATE, false,
                    user.getEmail(),
                    user.getName(),
                    user.getPassword(),
                    user.getCompany(),
                    user.getPhone(),
                    user.getIs_active(),
                    user.getIs_admin(),
                    user.getId()
            );
            int status = preparedStatement.executeUpdate();

            if (status == 0) {
                throw new DAOException("Échec de la modification de l'utilisateur.");
            }
        } catch (SQLException e) {
            throw new DAOException(e);
        } finally {
            silentClosures(preparedStatement, conn);
        }
    }

    public void changeUserActivity(Long id) throws DAOException {
        Connection conn = null;
        PreparedStatement preparedStatement = null;

        try {
            conn = daoFactory.getConnection();
            preparedStatement = initPreparedStatement(conn, SQL_CHANGE_ACTIVITY, false, id);
            int status = preparedStatement.executeUpdate();

            if (status == 0) {
                throw new DAOException("Echec lors de la modification du status utilisateur.");
            }
        } catch (SQLException e) {
            throw new DAOException(e);
        } finally {
            silentClosures(preparedStatement, conn);
        }
    }

    public Long getGlobalStats(Long id) throws DAOException {
        Connection conn = null;
        PreparedStatement preparedStatement = null;
        ResultSet resultSet = null;
        Long entity = null;

        try {
            /* Récupération d'une connexion depuis la Factory */
            conn = daoFactory.getConnection();
            preparedStatement = initPreparedStatement(conn, SQL_STATS_BY_USER, false, id);
            resultSet = preparedStatement.executeQuery();

            /* Parcours de la ligne de données de l'eventuel ResultSet retourné */
            if (resultSet.next()) {
                entity = resultSet.getLong("global_stats");
            }
        } catch (SQLException e) {
            throw new DAOException(e);
        } finally {
            silentClosures(resultSet, preparedStatement, conn);
        }

        return entity;
    }


    public Timestamp getAVGTime(Long id) throws DAOException {
        Connection conn = null;
        PreparedStatement preparedStatement = null;
        ResultSet resultSet = null;
        Timestamp entity = null;

        try {
            /* Récupération d'une connexion depuis la Factory */
            conn = daoFactory.getConnection();
            preparedStatement = initPreparedStatement(conn, SQL_AVG_TIME, false, id);
            resultSet = preparedStatement.executeQuery();

            /* Parcours de la ligne de données de l'eventuel ResultSet retourné */
            if (resultSet.next()) {
                entity = resultSet.getTimestamp("avg_time");
            }
        } catch (SQLException e) {
            throw new DAOException(e);
        } finally {
            silentClosures(resultSet, preparedStatement, conn);
        }

        return entity;
    }

    public Timestamp getSumTime(Long id) throws DAOException {
        Connection conn = null;
        PreparedStatement preparedStatement = null;
        ResultSet resultSet = null;
        Timestamp entity = null;

        try {
            /* Récupération d'une connexion depuis la Factory */
            conn = daoFactory.getConnection();
            preparedStatement = initPreparedStatement(conn, SQL_SUM_TIME, false, id);
            resultSet = preparedStatement.executeQuery();

            /* Parcours de la ligne de données de l'eventuel ResultSet retourné */
            if (resultSet.next()) {
                entity = resultSet.getTimestamp("sum_time");
            }
        } catch (SQLException e) {
            throw new DAOException(e);
        } finally {
            silentClosures(resultSet, preparedStatement, conn);
        }

        return entity;
    }
}

